String setTypeIcon(String type) {
  switch (type) {
    case 'fire':
      return 'G';
      break;
    case 'grass':
      return 'J';
      break;
    case 'water':
      return 'R';
      break;
    case 'rock':
      return 'P';
      break;
    case 'bug':
      return 'A';
      break;
    case 'normal':
      return 'M';
      break;
    case 'poison':
      return 'N';
      break;
    case 'electric':
      return 'D';
      break;
    case 'ground':
      return 'K';
      break;
    case 'ice':
      return 'L';
      break;
    case 'dark':
      return 'B';
      break;
    case 'fairy':
      return 'E';
      break;
    case 'psychic':
      return 'O';
      break;
    case 'fighting':
      return 'F';
      break;
    case 'ghost':
      return 'I';
      break;
    case 'flying':
      return 'H';
      break;
    case 'dragon':
      return 'C';
      break;
    case 'steel':
      return 'Q';
      break;
    default:
      return 'A';
  }
}
